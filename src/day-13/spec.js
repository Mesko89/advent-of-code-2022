import { part1, part2 } from './index';

describe('Day 13: Distress Signal', () => {
  describe('part1', () => {
    it('should solve part 1', () => {
      expect(
        part1([
          '[1,1,3,1,1]',
          '[1,1,5,1,1]',
          '',
          '[[1],[2,3,4]]',
          '[[1],4]',
          '',
          '[9]',
          '[[8,7,6]]',
          '',
          '[[4,4],4,4]',
          '[[4,4],4,4,4]',
          '',
          '[7,7,7,7]',
          '[7,7,7]',
          '',
          '[]',
          '[3]',
          '',
          '[[[]]]',
          '[[]]',
          '',
          '[1,[2,[3,[4,[5,6,7]]]],8,9]',
          '[1,[2,[3,[4,[5,6,0]]]],8,9]',
        ])
      ).toBe(13);
    });
  });
  describe('part2', () => {
    it('should solve part 2', () => {
      expect(
        part2([
          '[1,1,3,1,1]',
          '[1,1,5,1,1]',
          '',
          '[[1],[2,3,4]]',
          '[[1],4]',
          '',
          '[9]',
          '[[8,7,6]]',
          '',
          '[[4,4],4,4]',
          '[[4,4],4,4,4]',
          '',
          '[7,7,7,7]',
          '[7,7,7]',
          '',
          '[]',
          '[3]',
          '',
          '[[[]]]',
          '[[]]',
          '',
          '[1,[2,[3,[4,[5,6,7]]]],8,9]',
          '[1,[2,[3,[4,[5,6,0]]]],8,9]',
        ])
      ).toBe(140);
    });
  });
});
